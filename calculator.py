print("This is a calculator!")

a = float(input("First Operand : "))
b = float(input("Second Operand : "))

# ADD
print(a+b)
# SUBTRACT
print(abs(a-b))
# DIVIDE
print(a/b)
# MULTIPLY
print(a*b)
# FLOOR DIVISION (Dividing and then ignoring the decimal part of the value)
print(a//b)
# MODULUS (Remainder of Devision)
print(a%b)